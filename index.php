						Līvi bija latviešu roka grupa, kas pastāvēja laika posmā no 1976. līdz 2010. gadam. Līvi ir vienīgā rokgrupa   Latvijā, kurai ir uzcelts piemineklis. Tā tika dibināta Liepājā, satiekoties diviem mūziķiem: Jurim Pavītolam un Ērikam Ķiģelim (grupu ar šādu nosaukumu J. Pavītols izveidoja jau 1972. gadā, bet grupa drīz vien pajuka, jo Juris cieta avārijā un ilgu laiku ārstējās slimnīcā). Dziesma "Vairogi" bija "pirmo" Līvu repertuārā. Grupa tika dibināta jaunatvērtajā Metalurgu kultūras pilī, pieaicinot J. Pavītola sievu Ingrīdu Pavītolu, Andri Krūziņu no bijušās Imanta Kalniņa grupas "2XBBM", kā arī pils direktores Ausmas Grodumas dēlu Jāni Grodumu. Ēriks Ķiģelis mira 1985. gadā, viņa vietā nāca Ainars Virga. 1987. gadā bundzinieku Vilni Krieviņu nomainīja Ainara Virgas brālis Dainis Virga, bet 2003. gadā Dainis Virga mira un viņa vietā atgriezās Vilnis Krieviņš. "Līviem" kopā ir bijuši trīs sastāvi un trīs vadošie solisti. Līdz ar J. Groduma nāvi 2010. gada 15. jūnijā, Ainars Virga paziņoja par grupas "Līvi" darbības izbeigšanu. 
